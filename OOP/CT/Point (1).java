public class Point {
    private int X,Y;
    Point(int X, int Y){
        this.X = X;
        this.Y = Y;
    }

    public void setX(int x) {
        X = x;
    }

    public int getX() {
        return X;
    }

    public void setY(int y) {
        Y = y;
    }

    public int getY() {
        return Y;
    }


}
